def _build_raw_data_context(
    df,
    dep_row: int,
    arr_row: int,
) -> str:
    """Build raw data structure + sample values for integrity checks."""
    lines = ["## RAW DATA STRUCTURE\n"]

    # Column header validation
    lines.append("### Expected vs Actual Column Headers")
    lines.append(f"Total columns in file: {df.shape[1]}")
    lines.append("")

    headers = list(df.columns)
    for key, expected_name in COL_HEADERS.items():
        idx = COL.get(key)
        if idx is not None and idx < len(headers):
            actual = str(headers[idx])
            match = "OK" if actual == expected_name else f"MISMATCH (got: '{actual}')"
            lines.append(f"  {key} (col {idx}): expected '{expected_name}' → {match}")

    # Raw values at DEPARTURE and ARRIVAL rows for critical columns
    lines.append("\n### Critical Raw Values")
    critical_keys = [
        "datetime", "report_type", "voyage_no", "next_port", "fuel_mode",
        "distance", "steaming_hrs", "avg_speed", "ordered_speed",
        "mgo_rob", "lng_rob", "vlsfo_g1_rob", "vlsfo_g2_rob",
        "bf5_hours", "wind_force",
    ]

    for label, row_idx in [("DEPARTURE", dep_row), ("ARRIVAL", arr_row)]:
        lines.append(f"\n  {label} row (index {row_idx}):")
        for key in critical_keys:
            idx = COL.get(key)
            if idx is not None and row_idx < len(df):
                val = df.iloc[row_idx, idx]
                lines.append(f"    {key}: {val}")

    return "\n".join(lines)


def _build_computed_summary(
    voyage_result: dict,
    vessel_config: dict,
) -> str:
    """Build computed output summary for cross-validation."""
    computed = voyage_result["computed"]
    metadata = voyage_result["metadata"]
    auxiliary = voyage_result.get("auxiliary", {})
    segments = computed["segments"]
    totals = computed["totals"]

    lines = ["## COMPUTED OUTPUT\n"]

    # Voyage info
    lines.append("### Voyage Information")
    lines.append(f"  Voyage No: {metadata.get('voyage_no')}")
    lines.append(f"  Type: {metadata.get('voyage_type')}")
    lines.append(f"  Fuel Mode: {metadata.get('fuel_mode')}")
    lines.append(f"  Departure: {metadata.get('dep_datetime')}")
    lines.append(f"  Arrival: {metadata.get('arr_datetime')}")
    lines.append(f"  Discharge Port: {metadata.get('discharge_port')}")

    # Voyage totals
    lines.append("\n### Voyage Totals")
    lines.append(f"  Distance: {totals.get('distance', 0):.1f} nm")
    lines.append(f"  Duration: {totals.get('duration_days', 0):.2f} days")
    lines.append(f"  Avg Speed: {totals.get('actual_avg_speed', 0):.2f} kts")
    lines.append(f"  LNG consumed: {totals.get('lng_consumed', 0):.2f} m³")
    lines.append(f"  MGO consumed: {totals.get('mgo_consumed', 0):.2f} MT")
    lines.append(f"  VLSFO consumed: {totals.get('vlsfo_consumed', 0):.2f} MT")
    lines.append(f"  Net LNG: {totals.get('net_lng', 0):.2f} m³")
    lines.append(f"  Net MGO: {totals.get('net_mgo', 0):.2f} MT")
    lines.append(f"  Net VLSFO: {totals.get('net_vlsfo', 0):.2f} MT")
    lines.append(f"  Weather excl MGO: {totals.get('excl_mgo_weather', 0):.2f} MT")
    lines.append(f"  Weather excl LNG: {totals.get('excl_lng_weather', 0):.2f} m³")
    lines.append(f"  Weather excl VLSFO: {totals.get('excl_vlsfo_weather', 0):.2f} MT")

    # Per-segment data
    lines.append(f"\n### Segments ({len(segments)})")
    for i, seg in enumerate(segments):
        lines.append(f"\n  Segment {i+1}:")
        lines.append(f"    Distance: {seg.get('distance', 0):.1f} nm")
        lines.append(f"    Duration: {seg.get('duration_days', 0):.2f} days")
        lines.append(f"    Instructed Speed: {seg.get('instructed_speed', 0):.1f} kts")
        lines.append(f"    Actual Avg Speed: {seg.get('actual_avg_speed', 0):.2f} kts")
        lines.append(f"    Reference Speed: {seg.get('reference_speed', 0):.2f} kts")
        lines.append(f"    LNG: {seg.get('lng_consumed', 0):.2f} m³")
        lines.append(f"    MGO: {seg.get('mgo_consumed', 0):.2f} MT")
        lines.append(f"    VLSFO: {seg.get('vlsfo_consumed', 0):.2f} MT")
        lines.append(f"    Weather excl hours: {seg.get('excl_weather_hours', 0):.1f}")
        lines.append(f"    Weather excl MGO: {seg.get('excl_mgo_weather', 0):.2f} MT")
        lines.append(f"    Weather excl LNG: {seg.get('excl_lng_weather', 0):.2f} m³")

    # Daily breakdown
    lines.append("\n### Daily Breakdown")
    lines.append("  Date | Dist(nm) | Steam(hrs) | Speed(kts) | MGO_daily | LNG_daily | VLSFO_daily | BF5(hrs)")

    # Get daily_rows from the first segment's raw data
    for seg in segments:
        for dr in seg.get("daily_detail", seg.get("rows", [])):
            dt = str(dr.get("datetime", ""))[:10]
            lines.append(
                f"  {dt} | "
                f"{dr.get('distance', 0):.1f} | "
                f"{dr.get('steaming_hrs', 0):.1f} | "
                f"{dr.get('avg_speed', 0):.1f} | "
                f"{dr.get('mgo_daily', 0):.2f} | "
                f"{dr.get('lng_daily', 0):.2f} | "
                f"{dr.get('vlsfo_daily', 0):.2f} | "
                f"{dr.get('bf5_hours', 0):.1f}"
            )

    # Auxiliary data
    lines.append("\n### Auxiliary Data")
    lines.append(f"  Boiler MGO: {auxiliary.get('mgo_boiler_total', 0):.2f} MT")
    lines.append(f"  Pilot MGO: {auxiliary.get('mgo_pilot_total', 0):.2f} MT")
    lines.append(f"  Boiler VLSFO: {auxiliary.get('vlsfo_boiler_total', 0):.2f} MT")
    lines.append(f"  Pilot VLSFO: {auxiliary.get('vlsfo_pilot_total', 0):.2f} MT")
    lines.append(f"  GCU LNG: {auxiliary.get('gcu_lng_total', 0):.2f} m³")
    lines.append(f"  GCU used: {auxiliary.get('gcu_used', False)}")
    lines.append(f"  Cargo Density: {auxiliary.get('cargo_density')}")
    lines.append(f"  LCV: {auxiliary.get('lcv')}")
    lines.append(f"  Reliq Hours: {auxiliary.get('reliq_total_hours', 0):.1f}")

    # Vessel warranty table
    lines.append("\n### Vessel Warranty Speed/Consumption Table")
    sct = vessel_config.get("speed_consumption_table", {})
    lines.append("  Speed(kts) | Laden_Gas(MT/d) | Laden_Pilot(MT/d) | Ballast_Gas(MT/d) | Ballast_Pilot(MT/d)")
    for speed in sorted(sct.keys(), reverse=True):
        vals = sct[speed]
        lines.append(f"  {speed:.1f} | {vals[0]:.1f} | {vals[1]:.1f} | {vals[2]:.1f} | {vals[3]:.1f}")

    lines.append(f"\n  BOR Laden: {vessel_config.get('bor_laden_pct', 0)}%/day")
    lines.append(f"  BOR Ballast: {vessel_config.get('bor_ballast_pct', 0)}%/day")
    lines.append(f"  Gross Capacity: {vessel_config.get('gross_capacity_cbm', 0):.2f} cbm")
    lines.append(f"  FOE Factor: {vessel_config.get('foe_factor', 0)}")

    # Speed anomalies
    anomalies = computed.get("speed_anomalies", [])
    if anomalies:
        lines.append(f"\n### Speed Anomalies Detected ({len(anomalies)})")
        for a in anomalies:
            lines.append(
                f"  {str(a['datetime'])[:16]}: speed={a['avg_speed']:.1f} kts, "
                f"weighted_avg={a['weighted_avg']:.1f} kts"
            )

    return "\n".join(lines)


