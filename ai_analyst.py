def review_voyage(
    df,
    voyage_result: dict,
    vessel_config: dict,
) -> list[dict]:
    """
    Send voyage data to Claude for expert review.

    Parameters
    ----------
    df             : raw DataFrame
    voyage_result  : dict with "computed", "metadata", "auxiliary"
    vessel_config  : vessel configuration dict

    Returns
    -------
    list[dict] — alerts, each with severity/category/message/details.
    Returns empty list if API key is missing or call fails.
    """
    api_key = os.environ.get("ANTHROPIC_API_KEY", "")
    if not api_key:
        logger.warning(
            "ANTHROPIC_API_KEY not set — skipping AI review. "
            "Set it in .env to enable the AI analyst."
        )
        return []

    try:
        import anthropic
    except ImportError:
        logger.warning(
            "anthropic package not installed — skipping AI review. "
            "Run: pip install anthropic"
        )
        return []

    # Build context
    metadata = voyage_result["metadata"]
    dep_row = voyage_result["computed"]["totals"].get("dep_row")
    arr_row = voyage_result["computed"]["totals"].get("arr_row")

    # Fallback: get dep/arr rows from metadata if not in totals
    if dep_row is None:
        # Try to parse from segment_info
        seg_info = voyage_result["computed"].get("segment_info", [])
        if seg_info:
            dep_row = seg_info[0].get("start_row_idx", 0)
            arr_row = seg_info[-1].get("end_row_idx", len(df) - 1)

    # Build the data context for Claude
    raw_context = ""
    if dep_row is not None and arr_row is not None:
        raw_context = _build_raw_data_context(df, dep_row, arr_row)

    computed_summary = _build_computed_summary(voyage_result, vessel_config)

    user_message = (
        f"Please review this voyage performance report for Voyage "
        f"{metadata.get('voyage_no', 'Unknown')} "
        f"({metadata.get('voyage_type', '')}).\n\n"
        f"{raw_context}\n\n{computed_summary}"
    )

    # Call Claude API
    try:
        client = anthropic.Anthropic(api_key=api_key)
        response = client.messages.create(
            model=AI_ANALYST_MODEL,
            max_tokens=AI_ANALYST_MAX_TOKENS,
            temperature=AI_ANALYST_TEMPERATURE,
            system=SYSTEM_PROMPT,
            messages=[{"role": "user", "content": user_message}],
        )

        # Parse response
        content = response.content[0].text.strip()

        # Handle markdown code blocks
        if content.startswith("```"):
            content = content.split("\n", 1)[1] if "\n" in content else content
            if content.endswith("```"):
                content = content[:-3].strip()

        alerts = json.loads(content)

        if not isinstance(alerts, list):
            logger.warning("AI analyst returned non-list response: %s", type(alerts))
            return []

        logger.info(
            "AI review complete: %d alert(s) for Voyage %s",
            len(alerts), metadata.get("voyage_no"),
        )
        return alerts

    except json.JSONDecodeError as e:
        logger.warning("AI analyst returned invalid JSON: %s", e)
        return []
    except Exception as e:
        logger.warning("AI review failed: %s", e)
        return []
