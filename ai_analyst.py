"""
ai_analyst.py
=============
Claude-powered AI review layer for voyage performance reports.

Acts as a senior marine engineer / voyage analyst who:
  1. Validates raw data integrity (column headers, data types, value ranges)
  2. Cross-validates computed output against raw data and warranty specs
  3. Returns structured alerts for the user to review

Requires ANTHROPIC_API_KEY in environment (or .env file).
Gracefully skips review if API key is not set or call fails.
"""

